# This Video was based on Docstrings in Python
# They are very similar to comments but comment are ignored by compiler but docs cannnot be ignored
# They are written between '''    ''' these commas 
# They can be printed any time using syntax -: print(function_name.__doc__)
# Eg-:

def square(n):
    ''' Takes in a number n,return the square of n '''
    print(n**2)
square(5)
print(square.__doc__) 

