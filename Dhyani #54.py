# This Video is Based on 'is' Operator Vs '==' Operator in Python

# Eg-:
# In Case Of mutable Objects like Lists,etc

a=[1,2,3]
b=[1,2,3]
print(a is b)  # Exact Location of Object in Memory
print(a==b)  # Value

print("\n")

# In Case Of immutable Objects like Tuple,etc

a=(1,2,3)
b=(1,2,3)
print(a is b)
print(a==b)
