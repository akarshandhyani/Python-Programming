# This Video is based on Short Hand If-Else Statements in Python
# We can Use If Else statements in single line in python
# Eg-:

a=1
b=9
print("A") if a>b else print("B")

# We can also add multiple statement in one line
# Eg-:

print("A") if a>b else print("C") if a==b else print("All Out")
    
# It can be used of simple condition to reduce time but it may be difficult to use for complex conditions
